    <script>
        $(document).ready(function () {
            const komentarTabel = [];
            var currentQuantity = 0; // Tambahkan variabel currentQuantity

            function updateTotal(id) {
                var quantityInput = $("#quantityInput" + id);
                var totalElement = $("#totalHarga");
                var jumlahKuantitasElement = $("#jumlahKuantitas");

                var harga = 0;

                if (id == 1) {
                    harga = 220000;
                } else if (id == 2) {
                    harga = 15000;
                } else if (id == 3) {
                    harga = 20000;
                }

                currentQuantity = parseInt(quantityInput.val(), 10); // Perbarui currentQuantity
                var totalHarga = currentQuantity * harga;
                totalElement.text("Rp " + totalHarga.toLocaleString("id-ID"));
                jumlahKuantitasElement.text(currentQuantity);
            }

            $(".tambah-button").click(function () {
                var id = $(this).data("id");
                var quantityInput = $("#quantityInput" + id);
                var currentQuantity = parseInt(quantityInput.val(), 10);
                currentQuantity++;
                quantityInput.val(currentQuantity);
                updateTotal(id);
            });

            $(".kurangi-button").click(function () {
                var id = $(this).data("id");
                var quantityInput = $("#quantityInput" + id);
                var currentQuantity = parseInt(quantityInput.val(), 10);

                if (currentQuantity > 0) {
                    currentQuantity--;
                    quantityInput.val(currentQuantity);
                    updateTotal(id);
                }
            });
            
            $("#quantityInput1").on("input", function () {
            currentQuantity = parseInt($(this).val(), 10);
            updateTotal(1);
        });

            function submitKomentar() {
                var namaInput = $("#nama").val();
                var emailInput = $("#email").val();
                var komentarInput = $("#komentar").val();

                if ($.trim(namaInput) === "" || $.trim(emailInput) === "" || $.trim(komentarInput) === "") {
                    return;
                }

                var komentar = {
                    nama: namaInput,
                    email: emailInput,
                    review: komentarInput
                };

                // Perbarui daftar komentar
                var daftarKomentar = $("#daftar-komentar");
                var listItem = $("<li></li>").html("<strong>" + komentar.nama + "</strong> (" + komentar.email +
                    "): " + komentar.review);
                daftarKomentar.append(listItem);

                $("#nama").val("");
                $("#email").val("");
                $("#komentar").val("");
            }

            function pesanSekarang() {
                // Mendapatkan total pesanan
                var totalPesan = $("#total1").text();

                // Menampilkan pesan transaksi sukses
                alert("Transaksi berhasil! Total pesanan Anda: " + totalPesan);
            }

            const stars = $("#ratingStars span");

            // rating dengan bintang
            stars.click(function () {
                const rating = $(this).data('rating');
                $("#userRating").val(rating);

                stars.each(function (index) {
                    if (index < rating) {
                        $(this).text('★');
                    } else {
                        $(this).text('☆');
                    }
                });

                // Tambahkan kelas 'clicked' untuk mengubah warna bintang yang diklik menjadi kuning/gold
                stars.removeClass('clicked');
                stars.slice(0, rating).addClass('clicked');
            });

            $("#reviewForm").submit(function (event) {
                event.preventDefault();
                const userName = $("#userName").val();
                const userEmail = $("#userEmail").val();
                const userRating = $("#userRating").val();
                const userReview = $("#userReview").val();

                // Buat ulasan yang baru
                const reviewList = $("#reviewList");
                const reviewContent = $("<div></div>").addClass(
                    'bg-gray-50 p-4 rounded-lg mb-4 text-center');

                const userProfile = $("<div></div>").addClass('flex flex-col items-center mb-2');

                // foto
                const userAvatar = $("<img></img>").addClass('w-8 h-8 rounded-full mb-2');

                // Buat email acak untuk mendapatkan avatar acak dari Gravatar
                const randomEmail = Math.random().toString(36).substring(2) + "@example.com";
                const gravatarURL = `https://www.gravatar.com/avatar/${randomEmail}?d=identicon`;

                userAvatar.attr('src', gravatarURL);
                userProfile.append(userAvatar);

                // Nama
                const userNameElement = $("<p></p>").addClass('font-bold').text(userName);
                userProfile.append(userNameElement);

                reviewContent.append(userProfile);

                // Rating
                const userRatingElement = $("<div></div>").addClass(
                    'flex items-center justify-center mb-2');
                const ratingText = $("<p></p>");

                for (let i = 1; i <= 5; i++) {
                    const star = $("<span></span>").text(i <= userRating ? '★' : '☆').addClass(
                        'text-2xl text-yellow-500');
                    ratingText.append(star);
                }

                userRatingElement.append(ratingText);
                reviewContent.append(userRatingElement);

                // Reviewer's Email
                const userEmailElement = $("<p></p>").addClass('text-gray-700').text(userEmail);
                reviewContent.append(userEmailElement);

                const reviewText = $("<p></p>").addClass('text-gray-700').text(userReview);
                reviewContent.append(reviewText);

                // Tambahkan ulasan baru ke daftar ulasan
                reviewList.append(reviewContent);

                // Reset form
                $("#userName").val('');
                $("#userEmail").val('');
                stars.text('☆');
                $("#userReview").val('');
            });
        });

    </script>

